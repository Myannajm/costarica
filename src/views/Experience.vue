     <br>
    <p>Costa Rica is home to some of the most beautiful beaches, wildlife, and natural wonders in the world. It attracts thousands of visitors every year with it's beautiful beaches and friendly locals. Here, I've compiled some of my favorite places I visited while I was there. Personally, many of these spots are must-sees for even the smallest of trips, with a variety of activities for everyone's preferences.</p>
    <br> 
    <h2>1. Puerto Viejo</h2>
    <div id="photo"><img src="/images/costarica3.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>Located on the Caribbean coast near Panama, Puerto Viejo is popular with young people and backpackers because of its great beaches, surfing, and party atmosphere.The town is very lively and youll find something going on every night. Its probably the most popular destination on the Caribbean coast. There are also many quiet beach hotels around for those who are looking for some peace and quiet. There is also a jaguar rescue center nearby that rehabilitates all sorts of local wildlife and releases them back into the wild.</p>
    <h2>2.Cahuita</h2>
    <div id="photo"><img src="/images/costarica4.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>Cahuita, a tiny town situated right next to a stunning national park with the same name, is about an hour north of Puerto Viejo. Cahuita is a place to relax. Theres one bar in town that gets lively (sometimes its half full!), but for the most part, after a day of hiking, animal spotting, swimming, or surfing, most people just sit around and read.The region is known for its Afro-Caribbean influences, which you might notice in the food and local culture as you chill out with some dessert crepes or grilled chicken.</p>
    <h2>3. Tortuguero</h2>
    <div id="photo"><img src="/images/costarica5.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>Tortuguero (which means <q>Land of Turtles</q>) is the Costa Rican version of the Amazon rainforest. It dominates the northern coast, with Tortuguero National Park spanning over 77,000 acres. This massive area is a series of rivers and canals that crisscross the jungle.The biggest draws to this area are the large numbers of turtles (hence the name) that come to nest along the shoreline. The best time to see them nesting is in April and May, but if youre visiting during the off-season, youll still be able to go hiking and participate in canal cruises. However, theres lots of wildlife to see year-round.</p>
    <h2>4. Corcovado</h2>
    <div id="photo"><img src="/images/costarica6.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>Corcovado National Park is on the remote Osa Peninsula in southwestern Costa Rica. Established in 1975, the park covers an area of 424 square kilometers, making it the largest park in the country.Though more popular than it used to be, its still a very rugged, quiet, and off-the-beaten-path destination in a country where almost everything is on the beaten path. The peninsula is not easy to get to (which helps keep tourists away), but your efforts will be greatly rewarded with deserted beaches, tons of wildlife, great hiking, camping, and lots of marine life. Its one of the best places in the country.</p>
    <h2>5. Arenal</h2>
    <div id="photo"><img src="/images/costarica7.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>Sitting at an elevation of 1,670m, Arenal is one of Costa Ricas many volcanoes. It still erupts from time to time, and if youre lucky, youll be able to see lava flowing down the volcano. (Though, its less likely to happen as the volcano has been dormant for the last seven years). The area has tons of wonderful activities: theres an awesome waterfall (La Fortuna), the national park with great hiking, sunset views over the lake, caving, rafting options, and famous hot springs. With so much to do, its no wonder its one of the most visited places in the country.</p>
    <h2>6. Manuel Antonio</h2>
    <div id="photo"><img src="/images/costarica8.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>A popular beach destination on the Pacific coast, Manuel Antonios wide, white-sand beaches and warm blue waters arent the only attractions people come for. The nearby national park, which opened in 1972, sees over 150,000 visitors annually and is home to great hiking trails, a number of secluded beaches, and offers up the chance to view three different kinds of native monkeys.</p>
    <h2>7. Monteverde</h2>
    <div id="photo"><img src="/images/costarica9.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>This premier cloud forest is the home to the elusive quetzal birds. Most people come to Monteverde for a glimpse of this rare bird. The area is one of the largest eco-tourism hubs in the country, and over 250,000 people visit every year.Monteverde, which rests right on the continental divide, experiences high winds and unusual weather patterns. The entire area is very green and wet. Witness it all on a zip-line adventure through the tree canopy or explore some of the sky bridges in the area. Expect to pay around $50 USD per person for zip-lining.</p>
    <h2>8. Poas Volcano</h2>
    <div id="photo"><img src="/images/costarica10.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>A great day trip from San Jose, Poas Volcano is an active stratovolcano with twin calderas filled with sulfur lakes. The lakes are so still, youll look at your picture and think you painted the color on.The volcanos last erupted in 2019, and have actually erupted 40 times since 1828! There are some small trails around the area too. Arrive early in the morning to avoid the clouds closing in and ruining the view.</p>
    <h2>9. Santa Teresa</h2>
    <div id="photo"><img src="/images/costarica2.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>At the bottom of the Nicoya coast is the hippie backpacker town of Santa Teresa. This “town” is really nothing more than a beach with a road lined with eateries, yoga centers, surf shops, and hostels. Everyone gets up early to hit the waves, so the overall atmosphere in town is pretty relaxed. You wont find a lot of crazy parties here. Santa Teresa is a good place to lie on the beach, hang out with people, and relax. Because of the “chill” vibe, many people end up staying in Santa Teresa for weeks, and even months, on end.</p>
    <h2>10. Dominical</h2>
    <div id="photo"><img src="/images/costarica11.jpg" alt="costa rica beachfront" style= "height:400px; width:75%"></div>
    <p>Dominical is a tropical backpacker's haven, with dirt streets, beautiful beaches, cheap accommodation, casual open-air restaurants, and a great vibe. Domincal is one of the best places in Costa Rica for surfing. Surfers who come here find it hard to leave, and visits often turn into extended stays.</p>
